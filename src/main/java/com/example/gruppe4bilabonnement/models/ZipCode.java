package com.example.gruppe4bilabonnement.models;

public class ZipCode {
    private int zipCode;
    private String city;

    public ZipCode() {
    }

    public int getZipCode() {
        return zipCode;
    }

    public void setZipCode(int zipCode) {
        this.zipCode = zipCode;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
